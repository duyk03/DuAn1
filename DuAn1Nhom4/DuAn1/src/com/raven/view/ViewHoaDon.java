    public void loadTable(List<HoaDon> lst) {
        DefaultTableModel dtm = (DefaultTableModel) tblHoaDon.getModel();
        dtm.setRowCount(0);
        for (HoaDon hd : lst) {
            Object rowData[] = {
                hd.getMaHD(),
                hd.getMaNV(),
                hd.getTenNV(),
                hd.getTenKH(),
                hd.getMaSP(),
                hd.getTenXe(),
                hd.getMauSac(),
                hd.getXuatXu(),
                hd.getDonGia(),
                hd.getSoLuong(),
                hd.getSoTienGiam(),
                hd.getTongTien(),
                hd.getNgayThanhToan(),
                hd.getTrangThai() == 0 ? "Đã Thanh Toán" : "Chưa Thanh Toán",
                hd.getNote()

            };
            dtm.addRow(rowData);
        }
    }

