    public void XuatHoaDon(String idhd) {
        try {
            Hashtable map = new Hashtable();
            map.put("maHD", idhd);//
            JasperReport rpt = JasperCompileManager.compileReport("src/com/raven/view/XuatHoaDon.jrxml");
            //map.put("MaHoaDon", cbbMaHoaDonBangHDCT.getSelectedItem());
            java.sql.Connection con = DBConnect.getConnection();
            //  java.sql.Connection connection = null;
            JasperPrint p = JasperFillManager.fillReport(rpt, map, con);
            JasperViewer.viewReport(p, false);
        } catch (JRException ex) {
            Logger.getLogger(ViewHoaDon.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    private void tblHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHoaDonMouseClicked
        // TODO add your handling code here:

        int row = tblHoaDon.getSelectedRow();
        if (row == -1) {
            return;
        }

//      
        String maHD = tblHoaDon.getValueAt(row, 0).toString();
        int n = JOptionPane.showConfirmDialog(
                null,
                "Bạn có muốn xuất PDF HÓA ĐƠN không ?",
                "HÓA ĐƠN",
                JOptionPane.YES_NO_OPTION);
        if (n == 0) {
            XuatHoaDon(maHD);
        } else {

            String maNV = tblHoaDon.getValueAt(row, 1).toString();
            String tenNV = tblHoaDon.getValueAt(row, 2).toString();
            String tenKH = tblHoaDon.getValueAt(row, 3).toString();
            String maSP = tblHoaDon.getValueAt(row, 4).toString();
            String tenXe = tblHoaDon.getValueAt(row, 5).toString();
            String mauSac = tblHoaDon.getValueAt(row, 6).toString();
            String xuatXu = tblHoaDon.getValueAt(row, 7).toString();
            String donGia = tblHoaDon.getValueAt(row, 8).toString();
            String soLuong = tblHoaDon.getValueAt(row, 9).toString();
            String soTienGiam = tblHoaDon.getValueAt(row, 10).toString();
            String tongTien = tblHoaDon.getValueAt(row, 11).toString();
            String trangThai = tblHoaDon.getValueAt(row, 13).toString();
            String note = tblHoaDon.getValueAt(row, 14).toString();

            try {
                tblHoaDon.getSelectedRow();
                Date date = new SimpleDateFormat("yyyy-MM-dd").parse(tblHoaDon.getValueAt(row, 12).toString());
                txtNgayThanhToan.setDate(date);
            } catch (Exception e) {

            }
            txtMaHD.setText(maHD);
            txtMaNV.setText(maNV);
            txtTenNV.setText(tenNV);
            txtTenKH.setText(tenKH);
            txtMaSP.setText(maSP);
            txtTenXe.setText(tenXe);
            txtMauSac.setText(mauSac);
            txtXuatXu.setText(xuatXu);
            txtDonGia.setText(donGia);
            txtSoLuong.setText(soLuong);
            txtSoTienGiam.setText(soTienGiam);
            txtTongTien.setText(tongTien);
            txtNote.setText(note);

            if (trangThai == "Đã Thanh Toán") {
                cboTrangThai.setSelectedIndex(0);
            } else {
                cboTrangThai.setSelectedIndex(1);
            }
        }

    }//GEN-LAST:event_tblHoaDonMouseClicked

